# Polymorphism is same function behaving differently is different contexts
# there are three types of polymorphism in Python

# polymorphism in built-in Python functions! 
print(1+2+3) # 6
print("1"+"2"+"3") # "123"

len(range(100)) # 100
len("Length") # 6
len([1,3,6]) # 3

